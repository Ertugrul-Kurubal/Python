# Task 1
# Converting Celsius to Fahrenheit
temp_value = int(input("Enter Celsius value : "))
result = ((float(temp_value) * 1.8) + 32.0)  # From formula F = ((C*1.8)+32)
print("Fahrenheit :", int(result))

